# For example, the os.path module contains functions to work with file paths,
# and the os.listdir() function can be used to list files in a directory.
# The os.system() method allows you to execute system commands
# from your Python code.
# Windows 11 (PowerShell), Linux, Mac = clear
# Windows (old, cmd) = cls
